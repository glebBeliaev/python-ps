# **Описание**: Создайте программу для чтения JSON-файла и вывода значений определенного поля из всех объектов
#
# **Входные данные**: Файл 'products.json' с готовыми данными (создайте его заранее с массивом объектов)
#
# **Выходные данные**: Все значения указанного поля, выведенные в консоль по одному на строку
#
# **Ограничения**:
# - Используйте режим чтения 'r'
# - Обязательно указывайте encoding='utf-8'
# - Используйте конструкцию with для безопасной работы с файлом
# - Используйте json.load() для чтения JSON
# - Извлекайте поле 'name' из каждого объекта
#
# **Примеры**:
# Входные данные: Файл 'products.json' содержит:
# [
#   {"id": 1, "name": "Хлеб", "price": 25},
#   {"id": 2, "name": "Молоко", "price": 60},
#   {"id": 3, "name": "Сыр", "price": 120}
# ]
# Выходные данные:
# Хлеб
# Молоко
# Сыр

import json

# Ваш код здесь
with open("products.json", "r", encoding="utf-8") as f:
    data = json.load(f)
    for item in data:
        print(item["name"])
