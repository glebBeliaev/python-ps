# **Описание**: Создайте программу для записи текстовой заметки в файл
#
# **Входные данные**: Встроенная в код строка с текстом заметки
#
# **Выходные данные**: Файл 'note.txt' с записанным текстом
#
# **Ограничения**:
# - Используйте режим записи 'w'
# - Обязательно указывайте encoding='utf-8'
# - Используйте конструкцию with для безопасной работы с файлом
#
# **Примеры**:
# Входные данные: note_text = "Моя первая заметка"
# Выходные данные: Файл 'note.txt' содержит текст "Моя первая заметка"

# Встроенные данные
note_text = "Моя первая заметка"

# Ваш код здесь
with open("note.txt", "w", encoding="utf-8") as f:
    f.write(note_text)
