# **Описание**: Создайте программу для вычисления возраста человека в днях на основе его даты рождения
#
# **Входные данные**: Дата рождения задана в коде как объект date с годом, месяцем и днём
#
# **Выходные данные**: Количество полных дней от даты рождения до сегодняшнего дня
#
# **Ограничения**: Дата рождения не может быть в будущем
#
# **Примеры**:
# Входные данные: date(2000, 1, 1) при сегодняшней дате 2025-01-15
# Output: 9145
#
# Входные данные: date(1995, 6, 15) при сегодняшней дате 2025-01-15
# Output: 10806

from datetime import date

# Дата рождения
birth_date = date(2000, 1, 1)
birth_date2 = date(1995, 6, 15)

# Ваш код здесь

print((date.today() - birth_date).days)
print((date.today() - birth_date2).days)
