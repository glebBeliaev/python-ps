from datetime import datetime

# **Описание**: Создайте программу для форматирования даты в различные строковые представления
#
# **Входные данные**: Объект datetime с заданными годом, месяцем, днём, часом и минутой
#
# **Выходные данные**: Три строки с разными форматами:
# - Формат 1: ГГГГ-ММ-ДД
# - Формат 2: ЧЧ:ММ:СС
# - Формат 3: ДД/ММ/ГГГГ ЧЧ:ММ
#
# **Ограничения**: Используйте метод strftime для форматирования
#
# **Примеры**:
# Входные данные: datetime(2024, 3, 15, 14, 30, 45)
# Output:
# 2024-03-15
# 14:30:45
# 15/03/2024 14:30
#
# Входные данные: datetime(2023, 12, 1, 9, 5, 0)
# Output:
# 2023-12-01
# 09:05:00
# 01/12/2023 09:05

# Встроенные данные для тестирования
dt = datetime(2024, 3, 15, 14, 30, 45)

# Ваш код здесь

print(dt.strftime("%Y-%m-%d"))
print(dt.strftime("%H:%M:%S"))
print(dt.strftime("%d/%m/%Y %H:%M"))
