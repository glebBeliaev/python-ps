# **Описание**: Создайте функцию make_formatter, которая принимает строку-префикс и возвращает новую функцию для форматирования текста с этим префиксом.
#
# **Входные данные**: Функция make_formatter принимает один аргумент: prefix (строка-префикс). Возвращаемая функция принимает строку text.
#
# **Выходные данные**: Возвращаемая функция должна возвращать строку, состоящую из префикса, двоеточия, пробела и переданного текста
#
# **Ограничения**: prefix и text - строки длиной от 1 до 20 символов
#
# **Примеры**:
# Input: make_formatter("INFO"), затем вызов возвращенной функции с аргументом "система запущена"
# Output: "INFO: система запущена"
#
# Входные данные: make_formatter("ERROR"), затем вызов возвращенной функции с аргументом "файл не найден"
# Output: "ERROR: файл не найден"


def make_formatter(prefix):
    return lambda text: f"{prefix}: {text}"


formatter = make_formatter("INFO")
print(formatter("система запущена"))
