# **Описание**: Создайте программу для работы с коллекцией фильмов, которая демонстрирует итерацию по словарю с выводом ключей, значений и пар.
#
# **Входные данные**: Готовый словарь с информацией о фильмах (ключ - жанр, значение - название фильма)
#
# **Выходные данные**:
# - Все жанры из словаря
# - Все названия фильмов
# - Все пары жанр-фильм в формате "жанр: название"
#
# **Ограничения**: Используйте методы keys(), values() и items() для итерации по словарю
#
# **Примеры**:
# Входные данные: movies = {'драма': 'Титаник', 'комедия': 'Маска', 'фантастика': 'Матрица'}
# Выходные данные:
# Жанры: драма, комедия, фантастика
# Фильмы: Титаник, Маска, Матрица
# Пары: драма: Титаник, комедия: Маска, фантастика: Матрица

movies = {"драма": "Титаник", "комедия": "Маска", "фантастика": "Матрица"}

print("Жанры:", ", ".join(movies.keys()))
print("Фильмы:", ", ".join(movies.values()))
print("Пары:")
for genre, movie in movies.items():
    print(f"{genre}: {movie}")
