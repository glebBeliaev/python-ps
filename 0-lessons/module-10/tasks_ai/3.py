# **Описание**: Создайте программу для работы с информацией о товарах в интернет-магазине, которая демонстрирует добавление и изменение значений в словаре.
#
# **Входные данные**: Готовый словарь с базовой информацией о товаре и список новых характеристик для добавления
#
# **Выходные данные**:
# - Исходный словарь товара
# - Словарь после добавления новых характеристик
# - Словарь после изменения цены товара
#
# **Ограничения**: Используйте только присвоение по ключу для добавления и изменения значений
#
# **Примеры**:
# Входные данные: product = {'name': 'Ноутбук', 'price': 50000} и новые характеристики: brand='Dell', warranty=2
# Выходные данные:
# Исходный товар: {'name': 'Ноутбук', 'price': 50000}
# После добавления: {'name': 'Ноутбук', 'price': 50000, 'brand': 'Dell', 'warranty': 2}
# После изменения цены: {'name': 'Ноутбук', 'price': 45000, 'brand': 'Dell', 'warranty': 2}

# Исходный словарь товара
product = {"name": "Ноутбук", "price": 50000}  # type: ignore

# Ваш код здесь
print("Исходный товар:", product)  # type: ignore
product["brand"] = "Dell"
product["warranty"] = 2
print("После добавления:", product)  # type: ignore
product["price"] = 45000
print("После изменения цены:", product)  # type: ignore
