# **Описание**:
# Создайте программу для работы с коллекцией книг в библиотеке,
# которая демонстрирует удаление элементов из словаря с получением удалённого значения.
#
# **Входные данные**: Готовый словарь с информацией о книгах (ключ - ID книги, значение - название) и список ID книг для удаления
#
# **Выходные данные**:
# - Исходный словарь книг
# - Для каждого удаляемого ID: сообщение об удалении с названием книги или сообщение об отсутствии
# - Итоговый словарь после всех удалений
#
# **Ограничения**: Используйте только метод pop() для удаления элементов, не используйте del
#
# **Примеры**:
# Входные данные: books = {101: 'Война и мир', 102: 'Анна Каренина', 103: 'Преступление и наказание'} и ids_to_remove = [102, 104, 103]
# Выходные данные:
# Исходные книги: {101: 'Война и мир', 102: 'Анна Каренина', 103: 'Преступление и наказание'}
# Удалена книга ID 102: Анна Каренина
# Книга ID 104: не найдена
# Удалена книга ID 103: Преступление и наказание
# Оставшиеся книги: {101: 'Война и мир'}

# Готовые данные
books = {101: "Война и мир", 102: "Анна Каренина", 103: "Преступление и наказание"}
ids_to_remove = [102, 104, 103]

print("Исходные книги:", books)

for id in ids_to_remove:
    if id in books:
        print(f"Удалена книга ID {id}: {books.pop(id)}")
    else:
        print(f"Книга ID {id}: не найдена")

print("Оставшиеся книги:", books)
