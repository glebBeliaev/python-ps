# **Описание**:
# Создайте программу для работы с коллекцией сотрудников компании,
# которая демонстрирует проверку подмножества и надмножества с использованием методов issubset и issuperset.
#
# **Входные данные**: Три готовых множества - отдел разработки, отдел тестирования и общий список всех сотрудников IT
#
# **Выходные данные**:
# - Все три множества
# - Результат проверки: является ли отдел разработки подмножеством общего IT отдела
# - Результат проверки: является ли отдел тестирования подмножеством общего IT отдела
# - Результат проверки: является ли общий IT отдел надмножеством для отдела разработки
# - Результат проверки: является ли общий IT отдел надмножеством для отдела тестирования
#
# **Ограничения**: Используйте только методы issubset() и issuperset() для проверок
#
# **Примеры**:
# Входные данные: dev_team = {'Алексей', 'Мария', 'Дмитрий'}, qa_team = {'Елена', 'Игорь'}, all_it = {'Алексей', 'Мария', 'Дмитрий', 'Елена', 'Игорь', 'Анна'}
# Выходные данные:
# Отдел разработки: {'Алексей', 'Мария', 'Дмитрий'}
# Отдел тестирования: {'Елена', 'Игорь'}
# Весь IT отдел: {'Алексей', 'Мария', 'Дмитрий', 'Елена', 'Игорь', 'Анна'}
# Разработка подмножество IT: True
# Тестирование подмножество IT: True
# IT надмножество разработки: True
# IT надмножество тестирования: True

# Готовые данные
dev_team = {"Алексей", "Мария", "Дмитрий"}
qa_team = {"Елена", "Игорь"}
all_it = {"Алексей", "Мария", "Дмитрий", "Елена", "Игорь", "Анна"}

# Ваш код здесь
print("Отдел разработки:", dev_team)
print("Отдел тестирования:", qa_team)
print("Весь IT отдел:", all_it)

print("Разработка подмножество IT:", dev_team.issubset(all_it))
print("Тестирование подмножество IT:", qa_team.issubset(all_it))
print("IT надмножество разработки:", all_it.issuperset(dev_team))
print("IT надмножество тестирования:", all_it.issuperset(qa_team))
