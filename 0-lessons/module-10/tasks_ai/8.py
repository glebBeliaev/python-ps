# **Описание**: Создайте программу для работы с коллекцией игровых персонажей, которая демонстрирует добавление и удаление элементов в множестве с использованием add, discard и remove.
#
# **Входные данные**: Готовое множество с именами персонажей и список операций для выполнения
#
# **Выходные данные**:
# - Исходное множество персонажей
# - Множество после каждой операции добавления
# - Результат операций удаления с сообщениями об успехе или ошибке
# - Итоговое множество персонажей
#
# **Ограничения**: Используйте методы add(), discard() и remove() для изменения множества. Обработайте ситуацию когда remove() вызывается для несуществующего элемента
#
# **Примеры**:
# Входные данные: characters = {'Воин', 'Маг', 'Лучник'} и операции: добавить 'Паладин', удалить 'Маг' через remove, удалить 'Вор' через remove, удалить 'Лучник' через discard
# Выходные данные:
# Исходные персонажи: {'Воин', 'Маг', 'Лучник'}
# Добавлен Паладин: {'Воин', 'Маг', 'Лучник', 'Паладин'}
# Удален Маг: {'Воин', 'Лучник', 'Паладин'}
# Ошибка: персонаж Вор не найден
# Удален Лучник: {'Воин', 'Паладин'}

characters = {"Воин", "Маг", "Лучник"}

# Ваш код здесь
print("Исходные персонажи: ", characters)
characters.add("Паладин")
print("Добавлен Паладин: ", characters)
characters.remove("Маг")
print("Удален Маг: ", characters)
try:
    characters.remove("Вор")
except KeyError:
    print("Ошибка: персонаж Вор не найден")
characters.discard("Лучник")
print("Удален Лучник: ", characters)
