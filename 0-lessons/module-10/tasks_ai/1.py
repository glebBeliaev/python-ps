# **Описание**:
# Создайте программу для работы с множествами,
# которая демонстрирует удаление дубликатов из списка
# и выполнение базовых операций над множествами.
#
# **Входные данные**: Два готовых списка чисел, содержащих дубликаты
#
# **Выходные данные**:
# - Уникальные элементы из первого списка
# - Уникальные элементы из второго списка
# - Объединение множеств
# - Пересечение множеств
# - Разность множеств (первое минус второе)
#
# **Ограничения**: Используйте только встроенные функции Python для работы с множествами
#
# **Примеры**:
# Входные данные: [1, 2, 2, 3, 4, 4] и [3, 4, 5, 5, 6]
# Выходные данные:
# Первое множество: {1, 2, 3, 4}
# Второе множество: {3, 4, 5, 6}
# Объединение: {1, 2, 3, 4, 5, 6}
# Пересечение: {3, 4}
# Разность: {1, 2}

# Исходные данные
list1 = [1, 2, 2, 3, 4, 4]
list2 = [3, 4, 5, 5, 6]

# Уникальные элементы из первого списка
unique_list1 = set(list1)
print("Первое множество:", unique_list1)

# Уникальные элементы из второго списка
unique_list2 = set(list2)
print("Второе множество:", unique_list2)

# Объединение множеств
union = unique_list1 | unique_list2
print("Объединение:", union)

# Пересечение множеств
intersection = unique_list1 & unique_list2
print("Пересечение:", intersection)

# Разность множеств (первое минус второе)
difference = unique_list1 - unique_list2
print("Разность:", difference)
