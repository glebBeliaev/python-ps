# **Описание**: Создайте программу для работы с коллекцией товаров в складской системе, которая демонстрирует очистку всех элементов множества и проверку его состояния.
#
# **Входные данные**: Готовое множество с кодами товаров на складе
#
# **Выходные данные**:
# - Исходное множество товаров
# - Количество товаров до очистки
# - Множество после очистки
# - Количество товаров после очистки
# - Проверка пустоты множества
#
# **Ограничения**: Используйте метод clear() для очистки множества и len() для подсчета элементов
#
# **Примеры**:
# Входные данные: warehouse = {'A001', 'B205', 'C103', 'D407', 'E299'}
# Выходные данные:
# Товары на складе: {'A001', 'B205', 'C103', 'D407', 'E299'}
# Количество товаров: 5
# Склад после очистки: set()
# Количество товаров: 0
# Склад пуст: True

# Готовые данные
warehouse = {"A001", "B205", "C103", "D407", "E299"}

# Ваш код здесь
print("Товары на складе:", warehouse)
print("Количество товаров:", len(warehouse))
warehouse.clear()
print("Склад после очистки:", warehouse)
print("Количество товаров:", len(warehouse))
print("Склад пуст:", len(warehouse) == 0)
