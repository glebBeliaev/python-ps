# **Описание**: Создайте программу для работы с коллекцией рецептов, которая демонстрирует копирование словаря и проверку равенства копий.
#
# **Входные данные**: Готовый словарь с рецептами (ключ - название блюда, значение - время приготовления в минутах)
#
# **Выходные данные**:
# - Исходный словарь рецептов
# - Скопированный словарь
# - Результат сравнения исходного и скопированного словарей
# - Словарь после изменения времени в копии
# - Результат сравнения после изменения
# - Исходный словарь (для проверки что он не изменился)
#
# **Ограничения**: Используйте метод copy() для создания копии словаря
#
# **Примеры**:
# Входные данные: recipes = {'борщ': 60, 'салат': 15, 'котлеты': 45}
# Выходные данные:
# Исходные рецепты: {'борщ': 60, 'салат': 15, 'котлеты': 45}
# Копия рецептов: {'борщ': 60, 'салат': 15, 'котлеты': 45}
# Словари одинаковые: True
# Копия после изменения: {'борщ': 60, 'салат': 10, 'котлеты': 45}
# Словари одинаковые: False
# Исходные рецепты: {'борщ': 60, 'салат': 15, 'котлеты': 45}

recipes = {"борщ": 60, "салат": 15, "котлеты": 45}

# Ваш код здесь
print("Исходные рецепты:", recipes)
recipes_copy = recipes.copy()
print("Копия рецептов:", recipes_copy)
print("Словари одинаковые:", recipes == recipes_copy)

recipes_copy["салат"] = 10
print("Копия после изменения:", recipes_copy)
print("Словари одинаковые:", recipes == recipes_copy)
print("Исходные рецепты:", recipes)
