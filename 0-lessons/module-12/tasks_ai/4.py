# **Описание**: Создайте функцию для обработки списка строк с использованием блока finally для завершающих действий
#
# **Входные данные**: Список строк для обработки
#
# **Выходные данные**: Количество успешно обработанных строк и завершающее сообщение
#
# **Ограничения**:
# - Используйте блок try для попытки обработки каждой строки
# - Используйте блок except для перехвата исключений
# - Используйте блок finally для вывода завершающего сообщения
# - Обрабатывайте каждую строку в цикле
# - Подсчитывайте количество успешно обработанных строк
# - В finally выведите "Обработка завершена"
#
# **Примеры**:
# Входные данные: ["hello", "world", "python"]
# Output: Обработано строк: 3
# Output: Обработка завершена
#
# Входные данные: ["test", "example"]
# Output: Обработано строк: 2
# Output: Обработка завершена


def process_strings(strings: list[str]):
    success_count = 0

    try:
        for string in strings:
            try:
                # Пытаемся обработать строку
                print(string.upper())
                success_count += 1
            except Exception:
                print("Ошибка при обработке строки")
    finally:
        print(f"Обработано строк: {success_count}")
        print("Обработка завершена")


# Тестовые данные
test_strings = ["hello", "world", "python"]
process_strings(test_strings)
