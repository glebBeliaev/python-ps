# **Описание**: Создайте функцию для обработки списка файлов с использованием блока else для выполнения действий при отсутствии ошибок
#
# **Входные данные**: Список строк с именами файлов
#
# **Выходные данные**: Количество файлов с корректными именами и список обработанных имен
#
# **Ограничения**:
# - Используйте блок try для проверки корректности имени файла
# - Имя файла считается корректным, если содержит точку и не пустое
# - При некорректном имени выбрасывайте ValueError с сообщением "Некорректное имя файла"
# - Используйте блок except для перехвата ValueError
# - Используйте блок else для добавления корректного имени в список результатов
# - При ошибке выведите "Пропущен файл: [имя_файла]"
# - Функция должна возвращать кортеж: (количество_корректных_файлов, список_корректных_имен)
# - Обрабатывайте каждое имя файла в цикле


def process_files(filenames):
    valid_files = []

    for filename in filenames:
        try:
            if filename == "" or "." not in filename:
                raise ValueError("Некорректное имя файла")

        except ValueError:
            print(f"Пропущен файл: {filename}")

        else:
            valid_files.append(filename)

    return len(valid_files), valid_files


# Тестовые данные
files = ["report.pdf", "photo.jpg", "document", "", "notes.txt"]

result = process_files(files)
print(result)
