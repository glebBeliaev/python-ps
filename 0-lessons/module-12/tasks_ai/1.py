# **Описание**: Создайте функцию для проверки возраста пользователя с обработкой ошибок
#
# **Входные данные**: Целое число - возраст пользователя
#
# **Выходные данные**: Строка с сообщением о результате проверки или сообщение об ошибке
#
# **Ограничения**:
# - Возраст должен быть от 0 до 150 лет включительно
# - При некорректном возрасте должно выбрасываться кастомное исключение
# - Функция должна обрабатывать исключения и возвращать соответствующие сообщения
#
# **Примеры**:
# Входные данные: 25
# Output: "Возраст корректный: 25 лет"
#
# Входные данные: -5
# Output: "Ошибка: Возраст не может быть отрицательным"
#
# Входные данные: 200
# Output: "Ошибка: Возраст не может быть больше 150 лет"


# Создайте кастомное исключение
class InvalidAgeError(Exception):
    pass


# Создайте функцию для проверки возраста
def validate_age(age: int):
    if age < 0:
        raise InvalidAgeError("Возраст не может быть отрицательным")
    elif age > 150:
        raise InvalidAgeError("Возраст не может быть больше 150 лет")
    if 0 <= age <= 150:
        return f"Возраст корректный: {age} лет"


# Создайте функцию для безопасной проверки возраста с обработкой ошибок
def check_age_safely(age: int):
    try:
        return validate_age(age)
    except InvalidAgeError as e:
        return str(e)


# Тестовые данные
test_ages = [25, -5, 200, 75, 0, 150]

# Ваш код для тестирования функций здесь
for age in test_ages:
    print(check_age_safely(age))
