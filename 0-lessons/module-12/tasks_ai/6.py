# **Описание**: Создайте функцию для обработки списка чисел с перехватом нескольких типов исключений
#
# **Входные данные**: Список строк, содержащих числа
#
# **Выходные данные**: Сумма всех корректно преобразованных чисел
#
# **Ограничения**:
# - Используйте блок try для попытки преобразования каждой строки в число
# - Используйте отдельные блоки except для ValueError и TypeError
# - При ValueError выведите "Пропущена некорректная строка: [строка]"
# - При TypeError выведите "Ошибка типа данных"
# - Функция должна возвращать сумму всех успешно преобразованных чисел
# - Обрабатывайте каждую строку в цикле
#
# **Примеры**:
# Входные данные: ["10", "20", "abc", "30"]
# Output: Пропущена некорректная строка: abc
# Output: 60
#
# Входные данные: ["5", "15", "xyz", "25"]
# Output: Пропущена некорректная строка: xyz
# Output: 45


def process_numbers(string_list: list[str]) -> int:
    total = 0

    for item in string_list:
        try:
            number = int(item)
            total += number

        except ValueError:
            print(f"Пропущена некорректная строка: {item}")

        except TypeError:
            print("Ошибка типа данных")

    return total


# Тестовые данные
test_data1 = ["10", "20", "abc", "30"]
test_data2 = ["5", "15", "xyz", "25"]

# Вызовы функции
result1 = process_numbers(test_data1)
print(result1)

result2 = process_numbers(test_data2)
print(result2)
