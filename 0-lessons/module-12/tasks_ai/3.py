# **Описание**: Создайте функцию для обработки пользовательского ввода с использованием блока else после try
#
# **Входные данные**: Строка, которую нужно преобразовать в целое число
#
# **Выходные данные**: Сообщение о результате операции
#
# **Ограничения**:
# - Используйте блок try для попытки преобразования строки в число
# - Используйте блок except для обработки ValueError
# - Используйте блок else для выполнения действий при успешном преобразовании
# - При успешном преобразовании выведите "Число успешно обработано: [число]"
# - При ошибке выведите "Ошибка преобразования: введена некорректная строка"
#
# **Примеры**:
# Входные данные: "42"
# Output: "Число успешно обработано: 42"
#
# Входные данные: "123"
# Output: "Число успешно обработано: 123"
#
# Входные данные: "abc"
# Output: "Ошибка преобразования: введена некорректная строка"


def process_string_input(input_string: str):
    try:
        number = int(input_string)
        print(f"Число успешно обработано: {number}")
    except ValueError:
        print("Ошибка преобразования: введена некорректная строка")


input_string = input("Введите строку: ")
process_string_input(input_string)
