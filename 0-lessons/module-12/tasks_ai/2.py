# **Описание**: Создайте функцию для безопасного деления двух чисел с обработкой исключения деления на ноль
#
# **Входные данные**: Два числа (делимое и делитель)
#
# **Выходные данные**: Результат деления или сообщение об ошибке
#
# **Ограничения**:
# - При делении на ноль должно перехватываться исключение ZeroDivisionError
# - Функция должна возвращать результат деления при корректных данных
# - При ошибке должно возвращаться сообщение "Деление на ноль невозможно"
#
# **Примеры**:
# Входные данные: 10, 2
# Output: 5.0
#
# Входные данные: 15, 3
# Output: 5.0
#
# Входные данные: 8, 0
# Output: "Деление на ноль невозможно"


def safe_divide(a: int, b: int):
    try:
        return a / b
    except ZeroDivisionError:
        return "Деление на ноль невозможно"


# Тестовые данные
test_cases = [(10, 2), (15, 3), (8, 0)]

for a, b in test_cases:
    result = safe_divide(a, b)
    print(f"Результат деления {a} на {b}: {result}")
