# **Описание**:
# Создайте функцию, которая принимает список чисел и возвращает новый список,
# содержащий только уникальные числа из исходного списка в порядке их первого появления.
#  Если исходный список пустой, функция должна вернуть пустой список.
#
# **Входные данные**: Список чисел (list)
#
# **Выходные данные**: Новый список с уникальными числами в порядке первого появления (list)
#
# **Ограничения**:
# - Функция должна принимать один обязательный аргумент
# - Не изменяйте исходный список
# - Используйте только базовые операции со списками
# - Сохраняйте порядок первого появления элементов
# - Если список пустой, верните пустой список
#
# **Примеры**:
# Input: remove_duplicates([1, 2, 2, 3, 1, 4])
# Output: [1, 2, 3, 4]
#
# Input: remove_duplicates([5, 5, 5])
# Output: [5]
#
# Input: remove_duplicates([])
# Output: []


def remove_duplicates(numbers: list[int]) -> list[int]:
    filtered_list: list[int] = []
    for num in numbers:
        if num not in filtered_list:
            filtered_list.append(num)
    return filtered_list


print(remove_duplicates([1, 2, 2, 3, 1, 4]))
print(remove_duplicates([5, 5, 5]))
print(remove_duplicates([]))
